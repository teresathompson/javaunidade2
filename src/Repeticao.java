public class Repeticao {
    public static void main(String[] args) {

        for (int i = 1; i <= 50; i++) {
            System.out.println(i + " Teresa");
        }

        int i = 1;
        while (i <= 2) {
            System.out.println(i + " Teresa");
            i++;

        }

        System.out.println("Fim do programa");

        do {
            System.out.println(i + " Teresa");
            i++;
        } while (i <= 4);
    }
}
